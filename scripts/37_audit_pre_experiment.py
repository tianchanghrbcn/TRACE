#!/usr/bin/env python3
"""Audit legacy pre-experiment files before Stage 3 migration."""

from __future__ import annotations

import argparse
import ast
import csv
import hashlib
import json
import re
from collections import Counter, defaultdict
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


CJK_RE = re.compile(r"[\u3400-\u4dbf\u4e00-\u9fff\uf900-\ufaff]")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Audit legacy pre-experiment candidates.")
    parser.add_argument(
        "--audit-csv",
        type=Path,
        default=Path("results/processed/legacy_audit_files.csv"),
        help="Legacy audit CSV generated by scripts/29_audit_legacy_sources.py.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("results/processed"),
        help="Directory for generated audit outputs.",
    )
    parser.add_argument(
        "--docs-output",
        type=Path,
        default=Path("docs/pre_experiment_migration_plan.md"),
        help="Markdown migration-plan output.",
    )
    return parser.parse_args()


def read_csv_rows(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        raise FileNotFoundError(f"Audit CSV not found: {path}")

    with path.open("r", encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def write_csv(path: Path, rows: list[dict[str, Any]], columns: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)

    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=columns, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def write_json(path: Path, data: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)

    with path.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


def sha1_file(path: Path) -> str:
    if not path.exists() or not path.is_file():
        return ""

    h = hashlib.sha1()
    with path.open("rb") as f:
        for chunk in iter(lambda: f.read(1024 * 1024), b""):
            h.update(chunk)

    return h.hexdigest()


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8-sig", errors="replace")


def ast_imports(text: str) -> list[str]:
    try:
        tree = ast.parse(text)
    except SyntaxError:
        return []

    imports: list[str] = []
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            imports.extend(item.name for item in node.names)
        elif isinstance(node, ast.ImportFrom):
            imports.append(node.module or "")

    return sorted(set(imports))


def regex_hits(patterns: list[str], text: str) -> list[str]:
    hits: list[str] = []
    for pattern in patterns:
        hits.extend(match.group(0) for match in re.finditer(pattern, text, flags=re.IGNORECASE))
    return sorted(set(hits))[:30]


def inspect_csv(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {
            "csv_columns": "",
            "csv_row_count": "",
            "alpha_columns": "",
            "metric_columns": "",
        }

    try:
        with path.open("r", encoding="utf-8-sig", newline="") as f:
            reader = csv.DictReader(f)
            rows = list(reader)
            columns = reader.fieldnames or []
    except Exception:
        return {
            "csv_columns": "",
            "csv_row_count": "",
            "alpha_columns": "",
            "metric_columns": "",
        }

    alpha_columns = [col for col in columns if "alpha" in col.lower()]
    metric_columns = [
        col for col in columns
        if any(key in col.lower() for key in ["median", "mean", "variance", "score", "combined", "sil", "db"])
    ]

    return {
        "csv_columns": "; ".join(columns),
        "csv_row_count": len(rows),
        "alpha_columns": "; ".join(alpha_columns),
        "metric_columns": "; ".join(metric_columns),
    }


def classify_candidate(path: Path, relative_path: str, file_name: str) -> str:
    lower = relative_path.lower().replace("\\", "/")
    name = file_name.lower()
    suffix = path.suffix.lower()

    if suffix == ".py":
        if "quick_weight" in name or "weight_scan" in name:
            return "weight_scan_script"
        if "alpha" in name or "graph" in name:
            return "alpha_plot_script"
        return "pre_experiment_script"

    if suffix == ".csv":
        if "alpha_metrics" in name:
            return "alpha_metrics_table"
        return "pre_experiment_table"

    if suffix == ".pdf":
        if "_zh" in name or "zh." in name:
            return "legacy_chinese_pdf"
        if "_en" in name or "en." in name:
            return "legacy_english_pdf"
        return "legacy_pdf"

    return "pre_experiment_reference"


def target_module(candidate_type: str) -> str:
    mapping = {
        "alpha_plot_script": "src/pre_experiment/alpha_plots.py",
        "weight_scan_script": "src/pre_experiment/alpha_metrics.py",
        "pre_experiment_script": "src/pre_experiment/alpha_metrics.py",
        "alpha_metrics_table": "results/pre_experiment/alpha_metrics.csv",
        "pre_experiment_table": "results/pre_experiment/",
        "legacy_english_pdf": "figures/pre_experiment/",
        "legacy_chinese_pdf": "do_not_migrate_to_release",
        "legacy_pdf": "figures/pre_experiment/",
    }
    return mapping.get(candidate_type, "review_manually")


def recommended_action(candidate_type: str) -> str:
    mapping = {
        "alpha_plot_script": "Migrate plotting logic into src/pre_experiment/alpha_plots.py with English labels.",
        "weight_scan_script": "Migrate alpha or weight-search logic into src/pre_experiment/alpha_metrics.py.",
        "pre_experiment_script": "Review and migrate reusable logic into src/pre_experiment.",
        "alpha_metrics_table": "Use as archived pre-experiment input and rebuild English figures from it.",
        "pre_experiment_table": "Review as possible pre-experiment data input.",
        "legacy_english_pdf": "Keep as legacy reference; regenerate final release figures from CSV when possible.",
        "legacy_chinese_pdf": "Do not include in reviewer-facing release; regenerate as English if needed.",
        "legacy_pdf": "Review manually before including in release.",
    }
    return mapping.get(candidate_type, "Review manually.")


def inspect_python(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {
            "imports": "",
            "input_calls": "",
            "output_calls": "",
            "plot_calls": "",
            "has_cjk": False,
        }

    text = read_text(path)

    input_calls = regex_hits(
        [
            r"pd\.read_csv\([^\n]+",
            r"pandas\.read_csv\([^\n]+",
            r"pd\.read_excel\([^\n]+",
            r"open\([^\n]+",
            r"json\.load\([^\n]+",
            r"np\.load\([^\n]+",
        ],
        text,
    )

    output_calls = regex_hits(
        [
            r"\.to_csv\([^\n]+",
            r"\.to_excel\([^\n]+",
            r"\.savefig\([^\n]+",
            r"plt\.savefig\([^\n]+",
            r"open\([^\n]+[\"']w[\"'][^\n]*",
        ],
        text,
    )

    plot_calls = regex_hits(
        [
            r"plt\.plot\(",
            r"\.plot\(",
            r"plt\.scatter\(",
            r"\.scatter\(",
            r"plt\.bar\(",
            r"\.bar\(",
            r"plt\.fill_between\(",
            r"\.fill_between\(",
            r"plt\.errorbar\(",
            r"seaborn",
        ],
        text,
    )

    return {
        "imports": "; ".join(ast_imports(text)),
        "input_calls": " || ".join(input_calls),
        "output_calls": " || ".join(output_calls),
        "plot_calls": "; ".join(plot_calls),
        "has_cjk": bool(CJK_RE.search(text)),
    }


def audit_one(row: dict[str, str]) -> dict[str, Any]:
    legacy_root = Path(row["legacy_root"])
    relative_path = row["relative_path"]
    source_path = legacy_root / relative_path
    file_name = row["file_name"]
    suffix = source_path.suffix.lower()

    exists = source_path.exists()
    digest = sha1_file(source_path) if exists else ""
    candidate_type = classify_candidate(source_path, relative_path, file_name)

    py_info = inspect_python(source_path) if suffix == ".py" else {
        "imports": "",
        "input_calls": "",
        "output_calls": "",
        "plot_calls": "",
        "has_cjk": False,
    }

    csv_info = inspect_csv(source_path) if suffix == ".csv" else {
        "csv_columns": "",
        "csv_row_count": "",
        "alpha_columns": "",
        "metric_columns": "",
    }

    language_hint = ""
    lower_name = file_name.lower()
    if "_zh" in lower_name:
        language_hint = "zh"
    elif "_en" in lower_name:
        language_hint = "en"

    return {
        "legacy_root": str(legacy_root),
        "relative_path": relative_path,
        "file_name": file_name,
        "exists": exists,
        "extension": suffix,
        "size_bytes": row.get("size_bytes", ""),
        "sha1": digest,
        "candidate_type": candidate_type,
        "language_hint": language_hint,
        "has_cjk": py_info["has_cjk"],
        "imports": py_info["imports"],
        "input_calls": py_info["input_calls"],
        "output_calls": py_info["output_calls"],
        "plot_calls": py_info["plot_calls"],
        "csv_columns": csv_info["csv_columns"],
        "csv_row_count": csv_info["csv_row_count"],
        "alpha_columns": csv_info["alpha_columns"],
        "metric_columns": csv_info["metric_columns"],
        "suggested_target": target_module(candidate_type),
        "recommended_action": recommended_action(candidate_type),
    }


def write_markdown(path: Path, rows: list[dict[str, Any]], summary: dict[str, Any]) -> None:
    by_type = defaultdict(list)
    for row in rows:
        by_type[row["candidate_type"]].append(row)

    lines: list[str] = []
    lines.append("# Pre-Experiment Migration Plan")
    lines.append("")
    lines.append("This document is generated by `scripts/37_audit_pre_experiment.py`.")
    lines.append("")
    lines.append("Stage 3.5 normalizes alpha and weight-selection pre-experiment artifacts.")
    lines.append("")
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- Generated at UTC: {summary['generated_at_utc']}")
    lines.append(f"- Total pre-experiment candidates: {summary['total_candidates']}")
    lines.append(f"- Unique content hashes: {summary['unique_sha1_count']}")
    lines.append("")
    lines.append("### Candidate type counts")
    lines.append("")
    lines.append("| Candidate type | Count |")
    lines.append("|---|---:|")
    for key, value in summary["candidate_type_counts"].items():
        lines.append(f"| {key} | {value} |")
    lines.append("")
    lines.append("## Migration rules")
    lines.append("")
    lines.append("1. Use `src/pre_experiment` for reusable code.")
    lines.append("2. Use `results/pre_experiment` for generated or archived pre-experiment tables.")
    lines.append("3. Use `figures/pre_experiment` for generated English figures.")
    lines.append("4. Do not expose Chinese PDF figures as reviewer-facing outputs.")
    lines.append("5. Regenerate final figures from CSV inputs whenever possible.")
    lines.append("")
    lines.append("## Candidate details")
    lines.append("")
    lines.append("| File | Type | Language | Target | Recommended action |")
    lines.append("|---|---|---|---|---|")
    for row in rows:
        lines.append(
            f"| {row['relative_path']} | {row['candidate_type']} | "
            f"{row['language_hint']} | {row['suggested_target']} | "
            f"{row['recommended_action']} |"
        )
    lines.append("")
    lines.append("## Next migration target")
    lines.append("")
    lines.append("The next step is to migrate `alpha_metrics.csv` and rebuild the alpha-vs-median and alpha-vs-variance figures with English labels.")
    lines.append("")

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    args = parse_args()

    audit_rows = read_csv_rows(args.audit_csv)
    pre_rows = [row for row in audit_rows if row.get("category") == "pre_experiment_candidate"]

    details = [audit_one(row) for row in pre_rows]

    candidate_type_counts = Counter(row["candidate_type"] for row in details)
    language_counts = Counter(row["language_hint"] or "unknown" for row in details)
    sha1_values = [row["sha1"] for row in details if row["sha1"]]

    duplicate_groups = defaultdict(list)
    for row in details:
        if row["sha1"]:
            duplicate_groups[row["sha1"]].append(row["relative_path"])

    duplicates = {
        digest: paths
        for digest, paths in duplicate_groups.items()
        if len(paths) > 1
    }

    summary = {
        "generated_at_utc": datetime.now(timezone.utc).isoformat(),
        "total_candidates": len(details),
        "candidate_type_counts": dict(sorted(candidate_type_counts.items())),
        "language_counts": dict(sorted(language_counts.items())),
        "unique_sha1_count": len(set(sha1_values)),
        "duplicate_groups": duplicates,
    }

    args.output_dir.mkdir(parents=True, exist_ok=True)
    detail_csv = args.output_dir / "pre_experiment_details.csv"
    summary_json = args.output_dir / "pre_experiment_summary.json"

    columns = [
        "legacy_root",
        "relative_path",
        "file_name",
        "exists",
        "extension",
        "size_bytes",
        "sha1",
        "candidate_type",
        "language_hint",
        "has_cjk",
        "imports",
        "input_calls",
        "output_calls",
        "plot_calls",
        "csv_columns",
        "csv_row_count",
        "alpha_columns",
        "metric_columns",
        "suggested_target",
        "recommended_action",
    ]

    write_csv(detail_csv, details, columns)
    write_json(summary_json, summary)
    write_markdown(args.docs_output, details, summary)

    print(json.dumps(summary, indent=2, ensure_ascii=False))
    print(f"[TRACE] Pre-experiment details written to: {detail_csv}")
    print(f"[TRACE] Pre-experiment summary written to: {summary_json}")
    print(f"[TRACE] Pre-experiment migration plan written to: {args.docs_output}")


if __name__ == "__main__":
    main()

