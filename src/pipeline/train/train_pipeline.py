"""
Run the cleaning-clustering pipeline.

This script reads results/eigenvectors.json, runs selected cleaners and
clusterers, and writes the original pipeline outputs:

- results/cleaned_results.json
- results/clustered_results.json
- results/analyzed_results.json
- results/logs/pipeline_run_manifest.json
- results/cleaned_data/
- results/clustered_data/

Reproducibility modes:

Mode B-smoke:
    Use --max-records, --cleaners, --clusterers, --cluster-trials, and --workers
    to run a small end-to-end pipeline check.

Mode C-full:
    Do not pass --max-records, --cleaners, --clusterers, or --cluster-trials.
    The default behavior runs all eigenvector records, all cleaning strategies,
    and all clustering methods.

The core cleaning and clustering algorithms are delegated to error_correction.py
and cluster_methods.py. This file only standardizes path handling, CLI options,
logging, and result manifests.
"""

